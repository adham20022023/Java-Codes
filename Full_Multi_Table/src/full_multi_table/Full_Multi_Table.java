package full_multi_table;

public class Full_Multi_Table {

    public static void main(String[] args) {
        for(int x=1;x<=12;x++)
        {
            for(int y=1;y<=12;y++)
            {
                System.out.println(x+" * "+y+"= "+x*y);
            }
            System.out.println("************************");
        }

    }
    
}
